-- Churn predictions enriched with customer context.
--
-- PREREQUISITE (DuckDB / dev):
--   Run notebooks/churn_prediction.ipynb to generate data/churn_predictions.csv.
--
-- PREREQUISITE (Databricks):
--   1. Run churn_prediction.ipynb and save predictions to a Delta table, OR
--   2. Upload churn_predictions.csv to DBFS and run:
--        spark.read.csv('dbfs:/FileStore/olist/churn_predictions.csv', header=True)
--          .write.format('delta').saveAsTable('cx_analytics.churn_predictions')
--   Then set DBT_CHURN_PREDICTIONS_TABLE=cx_analytics.churn_predictions

with predictions as (
    {% if target.type == 'databricks' %}
    -- On Databricks: read from Delta table (see prerequisite above)
    select
        customer_unique_id,
        cast(churn_probability as double)   as churn_probability,
        cast(predicted_label as int)        as predicted_label,
        model_name,
        cast(prediction_generated_at as timestamp) as prediction_generated_at
    from {{ env_var('DBT_CHURN_PREDICTIONS_TABLE', 'cx_analytics.churn_predictions') }}
    {% else %}
    -- On DuckDB: read from CSV file generated by churn_prediction.ipynb
    select *
    from read_csv_auto(
        'data/churn_predictions.csv',
        columns = {
            'customer_unique_id':          'VARCHAR',
            'churn_probability':           'DOUBLE',
            'predicted_label':             'INTEGER',
            'model_name':                  'VARCHAR',
            'prediction_generated_at':     'VARCHAR'
        }
    )
    {% endif %}
),

enriched as (
    select
        p.customer_unique_id,
        p.churn_probability,
        p.predicted_label,
        p.model_name,
        cast(p.prediction_generated_at as timestamp) as prediction_generated_at,

        -- Risk tier for prioritisation
        case
            when p.churn_probability >= 0.85 then 'critical'
            when p.churn_probability >= 0.65 then 'high'
            when p.churn_probability >= 0.40 then 'medium'
            else 'low'
        end as churn_risk_tier,

        -- Customer context
        d.state,
        d.order_frequency_segment,
        d.satisfaction_segment,
        d.total_orders,
        d.total_spend_brl,
        d.avg_order_value_brl,
        d.avg_review_score,
        d.avg_days_to_deliver,
        d.first_order_at,
        d.last_order_at,
        d.customer_lifespan_days

    from predictions p
    left join {{ ref('dim_customers') }} d using (customer_unique_id)
)

select * from enriched
